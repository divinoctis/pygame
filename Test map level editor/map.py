class Player: 
    def __init__(self, x, y, map_obj):
        self.x = x
        self.y = y
        self.map_obj = map_obj

    def move(self, dx, dy):
        new_x = self.x + dx
        new_y = self.y + dy

        if self.map_obj.map_data[new_y][new_x]!= '-1':
            self.x = new_x
            self.y = new_y

    def display_position(self, screen):
        pygame.draw.rect(screen, (255, 0, 0), pygame.Rect(self.x * 20, self.y * 20, 20, 20))

